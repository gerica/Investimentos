package br.cs.web.json;

public class JsonResult
{
  private Integer id;
  private String valor;
  
  public Integer getId()
  {
    return this.id;
  }
  
  public void setId(Integer id)
  {
    this.id = id;
  }
  
  public String getValor()
  {
    return this.valor;
  }
  
  public void setValor(String valor)
  {
    this.valor = valor;
  }
}
